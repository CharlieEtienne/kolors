    public function updateColorPalette(Request $request)
    {
        if ($request->has('color') AND $request->has('palette')) {
            $color = Color::find($request->color);
            $color->palette_id = $request->palette;
            $color->save();
            return response()->json(['success' => 'Color palette updated!']);
        }
